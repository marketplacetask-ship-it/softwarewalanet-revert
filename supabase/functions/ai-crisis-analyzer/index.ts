import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  // Handle CORS preflight
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      console.error("LOVABLE_API_KEY is not configured");
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("AI Crisis Analyzer - Processing request with", messages.length, "messages");

    const systemPrompt = `You are an AI Crisis Analyzer, an expert in incident management, system reliability, and crisis response. You help Incident & Crisis Response Managers by:

1. **Incident Analysis**: Analyze system incidents, identify patterns, and suggest root causes
2. **Impact Assessment**: Evaluate the impact of incidents on business operations and users
3. **Resolution Strategies**: Provide step-by-step resolution strategies and best practices
4. **Escalation Guidance**: Advise on when and how to escalate incidents
5. **Post-Mortem Support**: Help prepare incident post-mortems and prevent recurrence
6. **Security Assessment**: Identify potential security vulnerabilities and threats

Key Guidelines:
- Always prioritize user safety and system stability
- Provide actionable, specific recommendations
- Use clear, concise technical language
- Suggest immediate actions first, then long-term fixes
- Consider business impact in all recommendations
- Be direct and urgent when dealing with critical incidents

Format your responses with clear sections when appropriate:
- **Immediate Actions**: Steps to take right now
- **Root Cause Analysis**: Potential causes to investigate
- **Resolution Steps**: Detailed fix procedures
- **Prevention**: How to prevent future occurrences`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI Gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limits exceeded, please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    console.log("AI Crisis Analyzer - Streaming response back to client");

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("AI Crisis Analyzer error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
